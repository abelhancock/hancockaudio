exports.onCreateWebpackConfig = ({ stage, loaders, actions }) => {
  if (stage === "build-html") {
    actions.setWebpackConfig({
      module: {
        rules: [
          {
            test: "./src/components/NewContactForm",
            use: loaders.null(),
          },
        ],
      },
    })
  }
}

